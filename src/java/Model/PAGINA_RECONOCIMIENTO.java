public class PAGINA_RECONOCIMIENTO {

    private Integer ID_RECONOCIMIENTO;
    private String REC_DESC;
    private String REC_LUGAR;
    private String REC_ANO;

    public PAGINA_RECONOCIMIENTO() {
    }

    public Integer getID_RECONOCIMIENTO() {
        return ID_RECONOCIMIENTO;
    }

    public void setID_RECONOCIMIENTO(Integer ID_RECONOCIMIENTO) {
        this.ID_RECONOCIMIENTO = ID_RECONOCIMIENTO;
    }

    public String getREC_DESC() {
        return REC_DESC;
    }

    public void setREC_DESC(String REC_DESC) {
        this.REC_DESC = REC_DESC;
    }

    public String getREC_LUGAR() {
        return REC_LUGAR;
    }

    public void setREC_LUGAR(String REC_LUGAR) {
        this.REC_LUGAR = REC_LUGAR;
    }

    public String getREC_ANO() {
        return REC_ANO;
    }

    public void setREC_ANO(String REC_ANO) {
        this.REC_ANO = REC_ANO;
    }

}
