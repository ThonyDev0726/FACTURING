public class PAGINA_EXPERIENCIA {

    private Integer ID_EXPERIENCIA;
    private String EXP_DESC;
    private String EXP_LUGAR;
    private String EXP_ANO;

    public PAGINA_EXPERIENCIA() {
    }

    public Integer getID_EXPERIENCIA() {
        return ID_EXPERIENCIA;
    }

    public void setID_EXPERIENCIA(Integer ID_EXPERIENCIA) {
        this.ID_EXPERIENCIA = ID_EXPERIENCIA;
    }

    public String getEXP_DESC() {
        return EXP_DESC;
    }

    public void setEXP_DESC(String EXP_DESC) {
        this.EXP_DESC = EXP_DESC;
    }

    public String getEXP_LUGAR() {
        return EXP_LUGAR;
    }

    public void setEXP_LUGAR(String EXP_LUGAR) {
        this.EXP_LUGAR = EXP_LUGAR;
    }

    public String getEXP_ANO() {
        return EXP_ANO;
    }

    public void setEXP_ANO(String EXP_ANO) {
        this.EXP_ANO = EXP_ANO;
    }

}
